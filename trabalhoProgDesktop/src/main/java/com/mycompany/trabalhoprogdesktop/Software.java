    public boolean validarCampos(JTextField textField ,String nomeTextField){
        String campo = textField.getText().trim();
        
        if( campo.isEmpty()){
            JOptionPane.showMessageDialog(this, ""+nomeTextField +" está vazio! ");
            textField.requestFocus();
            return false;
        }return true;
    }
    
