    private void btSalvarOrcamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarOrcamentoActionPerformed
      
        if(!validarCampos( tfPeca, "Peça" )){
            return;}
        if(!validarNumeros(tfValorPeca, "Valor das Peças" )){
            return;}
        if(!validarNumeros( tfQuantidadePecas, "Quantidade de Peças" )){
            return;}        
        if(!validarNumeros(tfValorMaodeObra, "Mão de Obra")){
            return;}
        if(!validarCampos( tfServico, "Valor de Serviço" )){
            return;}
        
        servicos.setItem(tfPeca.getText());
        servicos.setServico(tfServico.getText());
        servicos.setValorPecaServico(tfValorPeca.getText());
        servicos.setQuantidadePecas(tfQuantidadePecas.getText());
        servicos.setValorMaodeObra(tfValorMaodeObra.getText());
        
       
        //Para informar ao cliente o valor de todas as pecas * quantidade, utilizei valorTotalPecas
        //Para informar ao cliente o valor das pecas * quantidade utilizei valorTotalPeca
        //Por isso duas variaveis (quase) iguais.
        servicos.setValorTotalPecas((Double.valueOf(servicos.getValorPecaServico()) * Double.valueOf(servicos.getQuantidadePecas())) + servicos.getValorTotalPecas());
        servicos.setValorTotalPeca((Double.valueOf(servicos.getValorPecaServico()) * Double.valueOf(servicos.getQuantidadePecas())));
        servicos.setValorTotalMaodeObra(Double.valueOf(servicos.getValorMaodeObra()) + servicos.getValorTotalMaodeObra());
        
        if( linhaSelecionada >= 0 ){
            modelo.removeRow(linhaSelecionada);
            modelo2.removeRow(linhaSelecionada);
            modelo.insertRow(linhaSelecionada, new Object[] {servicos.getItem(),servicos.getServico() ,servicos.getValorPecaServico(), servicos.getQuantidadePecas(), servicos.getValorMaodeObra()});
            modelo2.insertRow(linhaSelecionada, new Object[] {servicos.getItem(),servicos.getServico() ,servicos.getValorPecaServico(), servicos.getQuantidadePecas(), servicos.getValorTotalPeca(), servicos.getValorMaodeObra()});
        }else{
            modelo.addRow(new Object[] {servicos.getItem(),servicos.getServico() ,servicos.getValorPecaServico(), servicos.getQuantidadePecas(), servicos.getValorMaodeObra()});
            modelo2.addRow(new Object[] {servicos.getItem(),servicos.getServico() ,servicos.getValorPecaServico(), servicos.getQuantidadePecas(), servicos.getValorTotalPeca(), servicos.getValorMaodeObra()});
        }

        JOptionPane.showMessageDialog(jPanel2, "Orcamento salvo com Sucesso!");

        tfPeca.setText("");
        tfValorPeca.setText("");
        tfQuantidadePecas.setText("");
        tfValorMaodeObra.setText("");
        tfServico.setText("");
        tfPeca.requestFocus();
        tfValorPeca.requestFocus();
        tfQuantidadePecas.requestFocus();
        tfValorMaodeObra.requestFocus();
        linhaSelecionada = -1;
        
        tfSubTotal.setText("R$ " + String.valueOf(servicos.getValorTotalPecas() + servicos.getValorTotalMaodeObra()));
        servicos.setSubTotal(tfSubTotal.getText());
    }//GEN-LAST:event_btSalvarOrcamentoActionPerformed

